
/**
 * The allowed truck movement types.
 */
export enum TruckMovimentTypes {
    /**
     * Forward motion
     */
    FORWARD,

    /**
     * Backwards motion
     */
    BACKWARDS,

    /**
     * Left motion
     */
    LEFT,

    /**
     * Right motion
     */
    RIGHT,

    /**
     * Unknown motion
     */
    UNKNOWN
}